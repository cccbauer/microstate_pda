# 06_stats_microstate_pda.py
# Run locally: python 06_stats_microstate_pda.py
# Deploys cluster script, submits SLURM job.
#
# What it does on the cluster:
#   PRIMARY   — Two-stage t-test per microstate
#               Per-subject Welch t-test (PDA+ vs PDA- TRs) + BH-FDR
#               Group one-sample t-test on subject-level mean differences
#   CONFIRMATORY — LMM per microstate (statsmodels MixedLM)
#               T_hat ~ pda_sign + C(task) + (pda_sign | subject)
#               Random slope captures between-subject heterogeneity
#               Interaction model tests if effect differs by task
#   COMBINATION — Per-subject logistic regression LORO CV (feedback runs)
#               AUC + discriminant weights across all 7 microstates
#
# Outputs (CLUSTER_BASE/stats/):
#   individual_stats_250Hz.csv     per-subject t-test × microstate × task-set
#   group_stats_250Hz.csv          group t-test + LMM fixed effects
#   combination_logistic_250Hz.csv logistic LORO AUC + weights per subject
#   group_stats_250Hz.png          Panel A: t-test bars | Panel B: LMM forest

